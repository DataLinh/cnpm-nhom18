package com.mycompany.cnpm.entities;

import javax.persistence.*;
import java.util.Date;
import java.util.HashSet;
import java.util.Set;

@Entity
@Table(name = "Users")
public class User {

    @Id
    @Column(name = "id")
    private String username;

    @Column
    private String password;

    @Column
    private String fullname;

    @Column
    private String email;

    @Column
    private Date birthday;

    @Column
    private String address;

    @Column
    private String phone;

    @Column
    private String schoolyear;

    @Column
    private String role;

    @OneToOne
    @JoinColumn(name = "maNganh")
    private Nganh nganh;
    
    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "maHoiDong")
    private HoiDong hoiDong;

    public User(){}
    
    /**
     * @return the username
     */
    public String getUsername() {
        return username;
    }

    /**
     * @param username the username to set
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * @return the fullname
     */
    public String getFullname() {
        return fullname;
    }

    /**
     * @param fullname the fullname to set
     */
    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the birthday
     */
    public Date getBirthday() {
        return birthday;
    }

    /**
     * @param birthday the birthday to set
     */
    public void setBirthday(Date birthday) {
        this.birthday = birthday;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * @return the schoolyear
     */
    public String getSchoolyear() {
        return schoolyear;
    }

    /**
     * @param schoolyear the schoolyear to set
     */
    public void setSchoolyear(String schoolyear) {
        this.schoolyear = schoolyear;
    }

    /**
     * @return the role
     */
    public String getRole() {
        return role;
    }

    /**
     * @param role the role to set
     */
    public void setRole(String role) {
        this.role = role;
    }

    /**
     * @return the nganh
     */
    public Nganh getNganh() {
        return nganh;
    }

    /**
     * @param nganh the nganh to set
     */
    public void setNganh(Nganh nganh) {
        this.nganh = nganh;
    }

    /**
     * @return the hoiDong
     */
    public HoiDong getHoiDong() {
        return hoiDong;
    }

    /**
     * @param hoiDong the hoiDong to set
     */
    public void setHoiDong(HoiDong hoiDong) {
        this.hoiDong = hoiDong;
    }


}
